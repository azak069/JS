const orang = ["Davy", "D", "Jones"];

for (const nama of orang){
    console.info(nama);
}; // mengakses semua nilai dalam array(bukan indeks) dengan perulangan

const namaLengkap = "Davy D Jones";

for (const kar of namaLengkap){
    console.info(kar);
}; // bisa mengakses karakter dalam string langsung

// Program ini mendemonstrasikan penggunaan perulangan for of untuk mengakses nilai