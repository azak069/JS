const nilai = 'E';

switch (nilai){
    case 'A':
        console.info("Luar Biasa King!");
        break;
    case 'B':
        console.info("Bagus Boss!")
        break;
    case 'C':
        console.info("B aja")
        break;
    case 'D':
        console.info("Belajar lagi dek!")
        break;
    default:
        console.info("Masih ada opsi pindah jurusan bang!")
}

// Program ini mendemonstrasikan penggunaan switch case sebagai alternatif if else if